        'telephone',
        'telephone_verified',
        'telephone_verified_at',
        'role',
        'mode',
        'email',
        'email_verified',
        'email_verified_at',
        'added_id',
        'added_date',
        'udpated_by',
        'status',
        'archived',
        'archived_id',
        'archived_by',
        'archived_date'
    ];

    /**
